/*
 * Define aliases for fixed-width types.
 * Created by shacking on 30/09/16.
 */
#ifndef __SGE_TYPES_H
#define __SGE_TYPES_H

#include <cstdint>

// Unsigned aliases
typedef uint8_t  u8;
typedef uint16_t u16;
typedef uint32_t u32;
typedef uint64_t u64;

// Signed aliases
typedef int8_t  s8;
typedef int16_t s16;
typedef int32_t s32;
typedef int64_t s64;

#endif /* __SGE_TYPES_H */
